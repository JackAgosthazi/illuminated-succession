#!/usr/bin/env python3
"""Emit data/shards/_IDS.md — the closed list of endpoint ids the links agents may
reference. Handing researchers the real ids is what stops a correlation from
pointing at a person or event that does not exist.

Run: python3 build/make-ids.py
"""
import glob
import json
import pathlib

ROOT = pathlib.Path(__file__).parent.parent
P, E = {}, {}
for f in sorted(glob.glob(str(ROOT / "data/shards/people-*.json"))):
    for r in json.loads(pathlib.Path(f).read_text()):
        P[r["id"]] = r
for f in sorted(glob.glob(str(ROOT / "data/shards/events-*.json"))):
    for r in json.loads(pathlib.Path(f).read_text()):
        E[r["id"]] = r


def yr(d):
    return int(str(d)[:4]) if d else 0


L = [
    "# Valid endpoint ids (generated by build/make-ids.py)",
    "",
    "Links may ONLY reference ids from this file. If a correlation needs a person or",
    "event that is not here, the link cannot be made — say so in your report instead",
    "of inventing an id.",
    "",
    "## People",
    "",
    "`id  |  name  |  birth-death  |  monarch / role`",
    "",
]
for p in sorted(P.values(), key=lambda p: yr((p.get("birth") or {}).get("date"))):
    b = (p.get("birth") or {}).get("date") or "?"
    d = (p.get("death") or {}).get("date") or "?"
    tag = "MONARCH" if p.get("monarch") else (p.get("role") or "")
    L.append(f"{p['id']}  |  {p['name']}  |  {b}-{d}  |  {tag}")

L += ["", "## Events", "", "`id  |  title  |  date`", ""]
for e in sorted(E.values(), key=lambda e: str(e["date"])):
    L.append(f"{e['id']}  |  {e['title']}  |  {e['date']}")

L += [
    "",
    "## Genealogical edges usable as link endpoints",
    "",
    'Descent: "<parentId>--<childId>" (double hyphen) — must match a parents entry.',
    'Marriage: "<aId>~<bId>" (tilde) — must match a spouses entry.',
    "",
    "### Marriages present in the data (use these exact strings)",
    "",
]
seen = set()
for p in P.values():
    for s in p.get("spouses") or []:
        sid = s.get("id")
        if sid in P:
            k = "~".join(sorted([p["id"], sid]))
            if k not in seen:
                seen.add(k)
                L.append(f"{k}   ({p['name']} & {P[sid]['name']}, m. {s.get('married') or '?'})")

L += ["", "### Descent edges present in the data", ""]
dseen = set()
for p in P.values():
    for par in p.get("parents") or []:
        if par in P:
            k = f"{par}--{p['id']}"
            if k not in dseen:
                dseen.add(k)
                L.append(f"{k}   ({P[par]['name']} -> {p['name']})")

out = ROOT / "data" / "shards" / "_IDS.md"
out.write_text("\n".join(L) + "\n")
print(f"wrote {out}: {len(P)} people, {len(E)} events, "
      f"{len(seen)} marriages, {len(dseen)} descent edges")
